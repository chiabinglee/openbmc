#!/bin/sh

# Write default VDD_VID 0x16 0x00
echo "====P8-4S Set Default CPU VID Starting===="
i2cset -y 4 0x60 0x16 0x00 b
i2cset -y 5 0x60 0x16 0x00 b
i2cset -y 6 0x60 0x16 0x00 b
i2cset -y 7 0x60 0x16 0x00 b


# Write default VCS_VID 0x17 0x00
i2cset -y 4 0x60 0x17 0x00 b
i2cset -y 5 0x60 0x17 0x00 b
i2cset -y 6 0x60 0x17 0x00 b
i2cset -y 7 0x60 0x17 0x00 b
echo "====P8-4S Set Default CPU VID Finish===="